package unit1.lesson2;

public class PrintingCircleProperties {
    public static void main(String[] args) {
        System.out.println("Given a circle with a radius of 5 units...");
        System.out.println("Diameter: " + (2 * 5) + " units");
        System.out.println("Circumference: " + (2 * 3.14 * 5) + " units");
        System.out.println("Area: " + (3.14 * (5 * 5)) + " units squared");
    }
}

